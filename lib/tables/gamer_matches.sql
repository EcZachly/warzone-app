CREATE TABLE gamer_matches (
    query_username TEXT,
    query_platform TEXT,
    match_id TEXT REFERENCES matches(match_id),
    username TEXT,
    team TEXT,
    clan_tag TEXT,
    kills INT,
    assists INT,
    deaths INT,
    score INT,
    damage_done INT,
    damage_taken INT,
    team_placement INT,
    longest_streak INT,
    time_played INT,
    distance_traveled REAL,
    team_survival_time BIGINT,
    percent_time_moving REAL,
    wall_bangs INT,
    gulag_deaths INT,
    gulag_kills INT,
    headshots INT,
    executions INT,
    objective JSON,
    xp JSON,
    loadout JSON,
    FOREIGN KEY (query_username, query_platform) REFERENCES gamers_new(username, platform),
    PRIMARY KEY (query_username, match_id)
);